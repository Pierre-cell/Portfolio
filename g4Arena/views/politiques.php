<h2>ARTICLE 3 : NATURE DES DONNEES COLLECTEES</h2>

Toutes les données concernant les Visiteurs sont collectées directement auprès de ces derniers.

La SAE POPB s’engage à recueillir le consentement préalable de ses Visiteurs et à leur permettre d’exercer leur droit d’opposition à l’utilisation de leurs données pour certaines finalités, dès que cela est nécessaire.</br>
<h2>ARTICLE 4 : FINALITE DES DONNEES COLLECTEES</h2>

Les Visiteurs sont toujours informés des finalités des données collectées via la présente Politique ou via les formulaires de collecte de données en ligne.

1. Nécessité de la collecte.

Lors de la réalisation des Services, le Visiteur communique certaines Données à Caractère Personnel. Si le Visiteur ne souhaite pas communiquer les informations qui lui sont demandées, il se peut que le Visiteur ne puisse pas accéder à certains Services (notamment des services d’accessibilité et certaines parties des sites internet et applications) et que SAE POPB soit dans l’impossibilité de répondre à sa demande.

 

2. Finalités

Les données des Visiteurs sont principalement traitées pour les finalités suivantes regroupées par familles :

Accueil et orientation des publics

- assurer l’accueil, l’orientation et la sécurité des Visiteurs au sein de l’AccorArena

- assurer l’accueil des PMR/PSH

Expérience client et informations marketing

- fournir aux Visiteurs des informations relatives à leurs achats et aux événements auxquels ils doivent assister (guide pratique, transports, mesures de sécurité spécifiques…)

- mener des campagnes de communication institutionnelles et commerciales (y compris envoyer des offres commerciales de prospection relatives à des produits similaires ou aux partenaires de l’AccorArena).

- mener des opérations promotionnelles et jeux concours

- Permettre aux Visiteurs de partager sur les réseaux sociaux des produits et services

- améliorer les Services et proposer de nouveaux services

- améliorer l’expérience des Visiteurs

- Communiquer de façon contextuelle (Geofencing)

- Personnaliser les Services et proposer aux Visiteurs des produits et services correspondant mieux à leurs besoins sur la base de leurs données déclaratives et de leurs préférences ;

- Personnaliser les Services et proposer aux Visiteurs des produits et services correspondant mieux à leurs besoins sur la base de leurs données personnelles, de leur comportements d'achat et du contrôle d'accès ;

- Développer la relation client (newsletter, édito, "ce qu'il ne fallait pas manquer", vœux, communications exceptionnelles, etc.) sur différents canaux (email, push, SMS) de façon ciblée et personnalisée ;

- l’utilisation du WIFI grand public

Relations clients

- traiter des demandes des Visiteurs

- traiter des litiges et réclamations des Visiteurs

Statistiques et enquêtes

- la mise en œuvre de mesures statistiques et de fréquentation des Services

- mener des enquêtes de satisfaction facultatives sur les Services

- Calcul d'indicateurs de performance billetterie et marketing

- Mesure de fréquentation et optimisation des flux à l’AccorArena

Gestion technique

- permettre leur navigation sur le Site et leur utilisation de l’Application

- Permettre aux Visiteurs de se connecter au WIFI grand public simplement et facilement sans avoir à créer un autre compte

- assurer la sécurité du Site et de l’Application

- la lutte contre la fraude

Le caractère obligatoire ou facultatif des Données à Caractère Personnel demandées est précisé lors de leur collecte.

 

3. Le recueil des Données à Caractère Personnel du Visiteur a pour base légale :

- l’exécution du contrat de vente en application des CGV du site de billetterie de G4 Arena ;

- l’intérêt légitime de SAE POPB à (i) fournir et améliorer les Services, (ii) promouvoir les Services et des nouveaux services, (iii) améliorer l’expérience client et (iv) lutter contre la fraude et sécuriser les Services ;

- les obligations imposées par la Législation ;

- le consentement des Visiteurs lorsque celui-ci est requis par la Législation.

 

<h2>ARTICLE 5 : NON-COMMUNICATION DES DONNÉES PERSONNELLES</h2>

Les Données à Caractère Personnel du Visiteur ne seront pas transmises à des acteurs commerciaux ou publicitaires sans le consentement du Visiteur.

Les Données à Caractère Personnel du Visiteur peuvent être traitées par des sous-traitants, concessionnaires et prestataires de services de la SAE POPB, dans le respect absolu du principe énoncé ci-dessus, exclusivement afin de réaliser les finalités de la présente politique.

Dans la limite de leurs attributions respectives et pour les finalités rappelées ci-dessus, les principales personnes susceptibles d’avoir accès aux données des Visiteurs de SAE POPB sont principalement les membres du personnel concernés de la SAE POPB ou des destinataires externes tels que :

- nos prestataires  et sous-traitants participants à la fourniture des Services ;

- les autorités compétentes.

 

<h2>ARTICLE 6 : POLITIQUE DE TRANSFERT DES DONNEES PERSONNELLES</h2>

La SAE POPB n’opère pas de transfert de Données à Caractère Personnel à des entités situées hors de l’Union européenne ou de l’Espace économique européen.

 

<h2>ARTICLE 7 : DUREE DE CONSERVATION DES DONNEES</h2>

SAE POPB conserve les Données à Caractère Personnel uniquement le temps nécessaire pour les finalités poursuivies, conformément aux prescriptions légales.

 

<h2>ARTICLE 8 : LES DROITS DES VISITEURS</h2>

Les Visiteurs peuvent consulter des copies de toutes les données personnelles détenues par la SAE POPB et demander à modifier, rectifier ou supprimer leurs données personnelles. Les Visiteurs peuvent également limiter, restreindre ou s’opposer au traitement de leurs données, ainsi que demander de leur fournir les Données à Caractère Personnel les concernant dans un format structuré, couramment utilisé et lisible par machine.

Les Visiteurs peuvent également demander la fin des sollicitations commerciales en retirant leur consentement à tout moment en cliquant sur le lien correspondant au bas de leur e-mail. Néanmoins, la SAE POPB pourra s’appuyer sur le consentement initial ou l’achat du Visiteur afin de réaliser un traitement sur les données collectées avant le retrait du consentement du Visiteur.

Pour exercer ses droits, le Visiteur pourra formuler sa demande en se connectant sur son compte ou bien en indiquant son nom, prénom, adresse mail, adresse postale, et en justifiant de son identité

- Par courrier électronique à l’adresse suivante : dataprivacy@g4.com

- Par courrier postal à l’adresse suivante : AccorArena – Données personnelles - 8 boulevard de Bercy, 75012 Paris

La demande du Visiteur sera traitée dans les délais légaux. La SAE POPB, afin de respecter des obligations légales de conservation, pourra stocker en archives intermédiaires certaines Données à Caractère Personnel.

 

