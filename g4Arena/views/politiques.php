    <p>POLITIQUE DE PROTECTION DES DONNÉES PERSONNELLES

applicable à compter du 25 mai 2018

 

<h2>ARTICLE 1 : OBJET</h2>

La Société anonyme d’exploitation du G4 Arena de Marseille (la « SAE POPB ») attache une importance particulière à la protection et à la confidentialité des données de ses visiteurs et utilisateurs de ses sites et applications mobiles (les « Visiteurs »). En tant que responsable de traitement, la SAE POPB s’engage à assurer la protection de ces Données à Caractère Personnel (telles que définies ci-après) par des moyens adéquats et sécurisés.

Par la présente politique de confidentialité des données personnelles (la « Politique »), la SAE POPB décrit les traitements des Données à Caractère Personnel effectués conformément à la législation en vigueur (Règlement général sur la protection des données n°2016/679 et la loi n° 78-17 du 6 janvier 1978 relative à l'informatique, aux fichiers et aux libertés modifiée (la « Législation »)),  sur (i) les données des Visiteurs en relation avec les services proposés par la SAE POPB à l’Accor Arena et (ii) sur les sites internet et applications mobiles qu’elle exploite, à savoir principalement le site internet http://www.accorarena.com (le « Site ») et l’application mobile « Accor Arena » (« l’Application »), hors pages relatives à la billetterie (ci-après ensemble les « Services »).

La Politique n’est pas applicable au site de billetterie fixe et mobile https://billetterie.accorarena.com ainsi qu’aux services qui ne sont pas rendus directement par la SAE POPB ou ses prestataires.

Par « Données à Caractère Personnel » on entend, au sens de la Législation, toute information concernant une personne physique identifiée ou identifiable ; est réputée identifiable une personne qui peut être identifiée, directement ou indirectement, notamment par référence à un numéro d’identification ou à un ou plusieurs éléments spécifiques, propres à son identité physique, physiologique, psychique, économique, culturelle ou sociale.

 

