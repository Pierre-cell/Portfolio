<h2>ARTICLE 12 : SÉCURITÉ</h2>

La SAE POPB met en œuvre toutes les mesures techniques, physiques, logiques et organisationnelles afin d’assurer la sécurité des traitements Données à Caractère Personnel et la confidentialité de données à caractère personnel.

A ce titre, la SAE POPB prend toutes les précautions utiles, au regard de la nature des données et des risques présentés par le traitement, afin de préserver la sécurité des données et, notamment, d’empêcher qu’elles soient déformées, endommagées, ou que des tiers non autorisés y aient accès par des politiques sur la sécurité physique (ex : contrôle de l’accès physique des locaux), sur la gestion des habilitations (ex : procédures garantissant la maitrise des droits d’accès aux données comme les modalités d’attribution et d’identification, ou la traçabilité et contrôle des accès), et sur les modalités de traitement de l’information (ex : cryptage des données, stockage sur des espaces dédiés)

 

<h2>ARTICLE 13 : INFORMATIONS PERSONNELLES ET MINEURS</h2>

En principe, le Site et l’Application s’adressent à des personnes majeures capables de contracter des obligations conformément à la législation du pays dans lequel se trouve le Visiteur.

Le Visiteur mineur de moins de 16 ans ou incapable doit obtenir le consentement préalable de son responsable légal préalablement à la saisie de ses données sur le Site et l’Application. Sauf obligation légale de confidentialité, la SAE POPB pourra directement informer ce responsable :

- des catégories spécifiques de Données à Caractère Personnel recueillies auprès de la personne mineure

-  de la possibilité de s’opposer à la collecte, l’utilisation ou la conservation de celles-ci.

 

