Cette Politique est susceptible d’évolutions ou de modifications. La SAE POPB invite et conseille ses Visiteurs à prendre régulièrement connaissance de la présente Politique. Néanmoins, les Données à Caractère Personnel du visiteur seront toujours traitées conformément à la politique en vigueur au moment de leur collecte, sauf si prescription légale.

Cette politique fait partie intégrante des Conditions Générales d’Utilisation du Site.

 

